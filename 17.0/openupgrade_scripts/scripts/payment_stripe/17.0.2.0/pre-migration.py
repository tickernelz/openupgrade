from openupgradelib import openupgrade


@openupgrade.migrate()
def migrate(env, version):
    openupgrade.logged_query(
        env.cr,
        """
        UPDATE payment_transaction
        SET provider_reference = stripe_payment_intent
        WHERE stripe_payment_intent IS NOT NULL
        """,
    )
